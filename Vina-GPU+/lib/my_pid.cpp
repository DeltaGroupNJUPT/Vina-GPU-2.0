/*
*如果定义了WIN32，将包含头文件Windows.h，否则包含头文件unistd.h
*/

#ifdef WINDOWS
#include <Windows.h>
#else
#include <unistd.h>
#endif

/*
*包含头文件my_pid.h
*/

#include "my_pid.h"

//如果定义了WIN32，利用GetCurrentProcessId()返回当前进程的进程ID，否则利用getpid()返回当前进程的进程ID
int my_pid() {
#ifdef WINDOWS
	return GetCurrentProcessId();
#else
	return getpid();
#endif
}

